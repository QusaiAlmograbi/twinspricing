async function migrateDisciplines() {
  try {
    const tableExists = await db.prepare(
      "SELECT name FROM sqlite_master WHERE type='table' AND name='disciplines'"
    ).get();
    if (!tableExists) return;

    const unclassified = await db.prepare(
      "SELECT id FROM disciplines WHERE name = 'Unclassified' LIMIT 1"
    ).get();

    if (unclassified) {
      console.log("[db] migrateDisciplines: cleaning up 'Unclassified' discipline...");
      await db.prepare("DELETE FROM discipline_sections WHERE discipline_id = ?").run(unclassified.id);
      await db.prepare("DELETE FROM disciplines WHERE id = ?").run(unclassified.id);
      console.log("[db] migrateDisciplines: removed 'Unclassified' discipline and its section links.");
    }
  } catch (err) {
    console.error("[db] migrateDisciplines error:", err.message || err);
  }
}

async function ensureSeeded() {
  const catCount = await db.prepare("SELECT COUNT(*) as count FROM price_categories").get();
  const catTotal = catCount?.count ?? 0;

  const itemCount = await db.prepare("SELECT COUNT(*) as count FROM price_items").get();
  const itemTotal = itemCount?.count ?? 0;

  console.log(`[db] ensureSeeded: ${catTotal} categories, ${itemTotal} items`);

  if (catTotal >= 10 && itemTotal > 0) {
    console.log("[db] ✓ Price list data verified.");
    return;
  }

  console.log("[db] ⚠ Insufficient price list data, re-seeding...");
  await seedDefaultTemplates();
  await smartMergeDefaultPriceList({ force: true });

  const recheckCats = await db.prepare("SELECT COUNT(*) as count FROM price_categories").get();
  const recheckItems = await db.prepare("SELECT COUNT(*) as count FROM price_items").get();
  const newCats = recheckCats?.count ?? 0;
  const newItems = recheckItems?.count ?? 0;

  if (newCats >= 10 && newItems > 0) {
    console.log(`[db] ✓ Re-seed successful: ${newCats} categories, ${newItems} items.`);
  } else {
    console.error(`[db] ✗ Re-seed FAILED: ${newCats} categories, ${newItems} items. Check server logs for errors.`);
  }
}

async function tableColumns(tableName) {
  if (isSqlite()) {
    const db = getSqliteDb();
    const info = db.prepare(`PRAGMA table_info(${tableName})`).all();
    return info.map((col) => ({ name: col.name }));
  }

  const result = await getPool().query(
    `SELECT column_name AS name FROM information_schema.columns WHERE table_schema = 'public' AND table_name = $1`,
    [tableName],
  );
  return result.rows;
}

async function ensureColumnIfMissing(tableName, columnName, definition) {
  const columns = await tableColumns(tableName);
  if (!columns.some((column) => column.name === columnName)) {
    await exec(
      `ALTER TABLE ${tableName} ADD COLUMN ${columnName} ${definition}`,
    );
    return true;
  }
  return false;
}

