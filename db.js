const { Pool } = require("pg");
const { getDatabaseConfig, resolveDatabasePath } = require("./config/database");

const config = getDatabaseConfig();
let pool = null;
let initializationPromise = null;

function getPool() {
  if (!pool) {
    if (!config.databaseUrl) {
      throw new Error(
        "DATABASE_URL is required for PostgreSQL. Set it before starting the app.",
      );
    }

    pool = new Pool({
      connectionString: config.databaseUrl,
      ssl:
        process.env.NODE_ENV === "production"
          ? { rejectUnauthorized: false }
          : false,
    });
  }

  return pool;
}

function normalizeQuery(query) {
  return query
    .replace(/datetime\(\s*'now'\s*\)/gi, "CURRENT_TIMESTAMP")
    .replace(/INSERT\s+OR\s+IGNORE\s+INTO/gi, "INSERT INTO")
    .replace(/AUTOINCREMENT/gi, "GENERATED BY DEFAULT AS IDENTITY");
}

function replaceQuestionMarks(query) {
  let index = 0;
  return query.replace(/\?/g, () => {
    index += 1;
    return `$${index}`;
  });
}

function splitStatements(query) {
  const statements = [];
  let current = "";
  let quote = null;

  for (let i = 0; i < query.length; i += 1) {
    const char = query[i];
    const nextChar = query[i + 1];

    if (quote) {
      current += char;
      if (char === quote && query[i - 1] !== "\\") {
        quote = null;
      }
      continue;
    }

    if (char === "'" || char === '"') {
      quote = char;
      current += char;
      continue;
    }

    if (char === ";") {
      const statement = current.trim();
      if (statement) {
        statements.push(statement);
      }
      current = "";
      continue;
    }

    if (char === "-" && nextChar === "-") {
      current += char;
      current += nextChar;
      i += 1;
      continue;
    }

    current += char;
  }

  const tail = current.trim();
  if (tail) {
    statements.push(tail);
  }

  return statements;
}

async function executeQuery(query, params = []) {
  const poolInstance = getPool();
  const sql = replaceQuestionMarks(normalizeQuery(query));
  const result = await poolInstance.query(sql, params);
  return result;
}

async function exec(query) {
  const statements = splitStatements(query);
  for (const statement of statements) {
    if (!statement) continue;
    await executeQuery(statement);
  }
}

function prepare(query) {
  const baseQuery = normalizeQuery(query);
  const sql = replaceQuestionMarks(baseQuery);

  return {
    async get(...params) {
      const result = await executeQuery(sql, params);
      return result.rows[0];
    },
    async all(...params) {
      const result = await executeQuery(sql, params);
      return result.rows;
    },
    async run(...params) {
      let statement = sql;
      if (/^insert\s+/i.test(statement) && !/\breturning\b/i.test(statement)) {
        statement = `${statement} RETURNING id`;
      }

      const result = await executeQuery(statement, params);
      const lastInsertRowid = result.rows?.[0]?.id ?? null;
      return {
        lastInsertRowid,
        rowCount: result.rowCount || 0,
      };
    },
  };
}

async function initializeDatabase() {
  if (initializationPromise) {
    return initializationPromise;
  }

  initializationPromise = (async () => {
    if (!config.databaseUrl) {
      return false;
    }

    await exec(`
      CREATE TABLE IF NOT EXISTS users (
        id BIGSERIAL PRIMARY KEY,
        name TEXT NOT NULL,
        email TEXT UNIQUE NOT NULL,
        password_hash TEXT NOT NULL,
        role TEXT NOT NULL DEFAULT 'designer',
        permissions TEXT NOT NULL DEFAULT '{}',
        created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
      );

      CREATE TABLE IF NOT EXISTS quotes (
        id BIGSERIAL PRIMARY KEY,
        user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
        project_name TEXT NOT NULL,
        data TEXT NOT NULL,
        total DOUBLE PRECISION NOT NULL DEFAULT 0,
        created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
        updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
      );

      CREATE TABLE IF NOT EXISTS project_access (
        id BIGSERIAL PRIMARY KEY,
        quote_id BIGINT NOT NULL REFERENCES quotes(id) ON DELETE CASCADE,
        user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
        granted_by BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
        permission TEXT NOT NULL DEFAULT 'view',
        created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
        UNIQUE(quote_id, user_id)
      );
    `);

    await ensureColumnIfMissing("users", "role", "TEXT NOT NULL DEFAULT 'designer'");
    await ensureColumnIfMissing("users", "permissions", "TEXT NOT NULL DEFAULT '{}'" );
    await ensureColumnIfMissing(
      "project_access",
      "permission",
      "TEXT NOT NULL DEFAULT 'view'",
    );

    await exec(`
      UPDATE users SET role = 'owner' WHERE LOWER(TRIM(COALESCE(role, ''))) = 'owner';
      UPDATE users SET role = 'admin' WHERE LOWER(TRIM(COALESCE(role, ''))) = 'admin';
      UPDATE users SET role = 'designer' WHERE role IS NULL OR TRIM(COALESCE(role, '')) = '' OR LOWER(TRIM(COALESCE(role, ''))) NOT IN ('owner', 'admin', 'designer');
      UPDATE users SET permissions = COALESCE(NULLIF(TRIM(permissions), ''), '{}') WHERE permissions IS NULL OR TRIM(COALESCE(permissions, '')) = '';
      UPDATE project_access SET permission = COALESCE(NULLIF(TRIM(permission), ''), 'view') WHERE permission IS NULL OR TRIM(COALESCE(permission, '')) = '';
    `);

    return true;
  })();

  return initializationPromise;
}

async function tableColumns(tableName) {
  const result = await getPool().query(
    `SELECT column_name AS name FROM information_schema.columns WHERE table_schema = 'public' AND table_name = $1`,
    [tableName],
  );
  return result.rows;
}

async function ensureColumnIfMissing(tableName, columnName, definition) {
  const columns = await tableColumns(tableName);
  if (!columns.some((column) => column.name === columnName)) {
    await exec(`ALTER TABLE ${tableName} ADD COLUMN ${columnName} ${definition}`);
  }
}

const db = {
  prepare,
  exec,
  query: executeQuery,
  initializeDatabase,
};

module.exports = db;
module.exports.getDatabaseConfig = getDatabaseConfig;
module.exports.resolveDatabasePath = resolveDatabasePath;
module.exports.initializeDatabase = initializeDatabase;
