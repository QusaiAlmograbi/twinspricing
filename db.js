const { Pool } = require("pg");
const { getDatabaseConfig, resolveDatabasePath } = require("./config/database");

const config = getDatabaseConfig();
let pool = null;
let sqliteDb = null;
let initializationPromise = null;
let Database = null;

function loadSqlite() {
  if (!Database) {
    Database = require("better-sqlite3");
  }
}

function isSqlite() {
  return config.client === "sqlite";
}

function getPool() {
  if (!pool) {
    if (!config.databaseUrl) {
      throw new Error(
        "DATABASE_URL is required for PostgreSQL. Set it before starting the app.",
      );
    }

    pool = new Pool({
      connectionString: config.databaseUrl,
      ssl:
        process.env.NODE_ENV === "production"
          ? { rejectUnauthorized: false }
          : false,
    });
  }

  return pool;
}

function getSqliteDb() {
  if (!sqliteDb) {
    loadSqlite();
    sqliteDb = new Database(config.sqlitePath);
    sqliteDb.pragma("journal_mode = WAL");
    sqliteDb.pragma("foreign_keys = ON");
  }
  return sqliteDb;
}

function normalizeQuery(query) {
  return query
    .replace(/datetime\(\s*'now'\s*\)/gi, "CURRENT_TIMESTAMP")
    .replace(/INSERT\s+OR\s+IGNORE\s+INTO/gi, "INSERT OR IGNORE INTO")
    .replace(/AUTOINCREMENT/gi, "AUTOINCREMENT")
    .replace(
      /GENERATED BY DEFAULT AS IDENTITY/gi,
      "AUTOINCREMENT",
    );
}

function replaceQuestionMarks(query) {
  let index = 0;
  return query.replace(/\?/g, () => {
    index += 1;
    return `$${index}`;
  });
}

function splitStatements(query) {
  const statements = [];
  let current = "";
  let quote = null;

  for (let i = 0; i < query.length; i += 1) {
    const char = query[i];
    const nextChar = query[i + 1];

    if (quote) {
      current += char;
      if (char === quote && query[i - 1] !== "\\") {
        quote = null;
      }
      continue;
    }

    if (char === "'" || char === '"') {
      quote = char;
      current += char;
      continue;
    }

    if (char === ";") {
      const statement = current.trim();
      if (statement) {
        statements.push(statement);
      }
      current = "";
      continue;
    }

    if (char === "-" && nextChar === "-") {
      current += char;
      current += nextChar;
      i += 1;
      continue;
    }

    current += char;
  }

  const tail = current.trim();
  if (tail) {
    statements.push(tail);
  }

  return statements;
}

function sqliteNormalize(query) {
  let q = query;
  q = q.replace(/TIMESTAMPTZ/gi, "TEXT");
  q = q.replace(/DOUBLE PRECISION/gi, "REAL");
  q = q.replace(/BIGSERIAL PRIMARY KEY/gi, "INTEGER PRIMARY KEY AUTOINCREMENT");
  q = q.replace(/BIGINT/gi, "INTEGER");
  q = q.replace(/datetime\(\s*'now'\s*\)/gi, "(datetime('now'))");
  q = q.replace(/CURRENT_TIMESTAMP/gi, "(datetime('now'))");
  q = q.replace(/INSERT INTO/gi, "INSERT OR IGNORE INTO");
  return q;
}

function pgNormalize(query) {
  let q = query;
  q = q.replace(/AUTOINCREMENT/gi, "GENERATED BY DEFAULT AS IDENTITY");
  q = q.replace(
    /INSERT\s+OR\s+IGNORE\s+INTO/gi,
    "INSERT INTO",
  );
  q = q.replace(/datetime\(\s*'now'\s*\)/gi, "CURRENT_TIMESTAMP");
  return q;
}

async function executeQuery(query, params = []) {
  if (isSqlite()) {
    const db = getSqliteDb();
    const sql = sqliteNormalize(query);
    const isSelect = /^\s*select/i.test(sql);

    if (isSelect) {
      const stmt = db.prepare(sql);
      const rows = stmt.all(...params);
      return { rows, rowCount: rows.length };
    } else {
      const stmt = db.prepare(sql);
      const result = stmt.run(...params);
      const row = db.prepare("SELECT last_insert_rowid() as id").get();
      return {
        rows: row ? [row] : [],
        rowCount: result.changes,
      };
    }
  }

  const poolInstance = getPool();
  const sql = pgNormalize(query);
  const result = await poolInstance.query(sql, params);
  return result;
}

async function exec(query) {
  if (isSqlite()) {
    const db = getSqliteDb();
    const statements = splitStatements(query);
    for (const statement of statements) {
      if (!statement) continue;
      const sql = sqliteNormalize(statement);
      db.exec(sql);
    }
    return;
  }

  const statements = splitStatements(query);
  for (const statement of statements) {
    if (!statement) continue;
    await executeQuery(statement);
  }
}

function prepare(query) {
  if (isSqlite()) {
    const db = getSqliteDb();
    const sql = sqliteNormalize(query);

    return {
      get(...params) {
        const stmt = db.prepare(sql);
        return stmt.get(...params) || undefined;
      },
      all(...params) {
        const stmt = db.prepare(sql);
        return stmt.all(...params);
      },
      run(...params) {
        const stmt = db.prepare(sql);
        const result = stmt.run(...params);
        const row = db.prepare("SELECT last_insert_rowid() as id").get();
        return {
          lastInsertRowid: row?.id ?? null,
          rowCount: result.changes,
        };
      },
    };
  }

  const baseQuery = pgNormalize(query);
  const sql = replaceQuestionMarks(baseQuery);

  return {
    async get(...params) {
      const result = await executeQuery(sql, params);
      return result.rows[0];
    },
    async all(...params) {
      const result = await executeQuery(sql, params);
      return result.rows;
    },
    async run(...params) {
      let statement = sql;
      if (/^insert\s+/i.test(statement) && !/\breturning\b/i.test(statement)) {
        statement = `${statement} RETURNING id`;
      }

      const result = await executeQuery(statement, params);
      const lastInsertRowid = result.rows?.[0]?.id ?? null;
      return {
        lastInsertRowid,
        rowCount: result.rowCount || 0,
      };
    },
  };
}

