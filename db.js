async function tableColumns(tableName) {
  if (isSqlite()) {
    const db = getSqliteDb();
    const info = db.prepare(`PRAGMA table_info(${tableName})`).all();
    return info.map((col) => ({ name: col.name }));
  }

  const result = await getPool().query(
    `SELECT column_name AS name FROM information_schema.columns WHERE table_schema = 'public' AND table_name = $1`,
    [tableName],
  );
  return result.rows;
}

async function ensureColumnIfMissing(tableName, columnName, definition) {
  const columns = await tableColumns(tableName);
  if (!columns.some((column) => column.name === columnName)) {
    await exec(
      `ALTER TABLE ${tableName} ADD COLUMN ${columnName} ${definition}`,
    );
    return true;
  }
  return false;
}

